package BasicProgramming;

public class Literals {

	public static void main(String args[])
	{
		int num = 0b101;
		System.out.println("Binary Literals:" +num);
		
		int num1 = 0x7E;
		System.out.println("Hexadecimal Literals:" +num1);
		
		double num2 = 14;
		System.out.println(num2);
		
		double num3 = 12e10;
		System.out.println(num3);
		
		char c = 'a';
		System.out.println(c);
	}
}
