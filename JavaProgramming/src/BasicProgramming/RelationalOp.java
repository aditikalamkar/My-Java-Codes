package BasicProgramming;

public class RelationalOp {
	
	public static void main(String args[])
	{
		int n1 = 6;
		int n2 = 8;
		
		boolean res = n1 < n2;
		boolean res1 = n1 > n2;
		boolean res2 = n1 == n2;
		boolean res3 = n1 != n2;
		boolean res4 = n1 <= n2;
		
		System.out.println(res);
		System.out.println(res1);
		System.out.println(res2);
		System.out.println(res3);
		System.out.println(res4);

	}

}
